# W tej lekcji omówimy listy i krotki w Pythonie.

# Lista to uporządkowana kolekcja elementów, które mogą mieć różne typy danych.
lista = [1, 2, 3, 4, 5]
print("Lista:", lista)

# Możemy zmieniać elementy listy:
lista[0] = 10
print("Zmieniona lista:", lista)

# Krotka to podobna kolekcja jak lista, ale jest niemutowalna (nie można zmieniać jej zawartości).
krotka = (1, 2, 3, 4, 5)
print("Krotka:", krotka)

# Podsumowanie lekcji:
# - **Listy** są uporządkowanymi kolekcjami danych, które mogą być zmieniane.
# - **Krotki** są uporządkowanymi kolekcjami danych, ale są niemutowalne (nie można ich zmieniać).
