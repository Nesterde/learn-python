# Zadanie 1: Zainicjalizuj repozytorium git w folderze z projektem.
# Wykonaj pierwsze commit i wypchnij zmiany na GitHub.

# 1. Otwórz terminal i wejdź do folderu projektu
# 2. Wykonaj polecenie `git init`
# 3. Dodaj pliki do repozytorium: `git add .`
# 4. Wykonaj commit: `git commit -m "Pierwszy commit"`
# 5. Utwórz repozytorium na GitHubie
# 6. Dodaj zdalne repozytorium: `git remote add origin <url_repozytorium>`
# 7. Wykonaj `git push -u origin master`
