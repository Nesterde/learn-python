# Git jest systemem kontroli wersji, który pozwala śledzić zmiany w projekcie.
# GitHub to serwis, który pozwala przechowywać i współdzielić projekty w chmurze.

# Polecenia Git:
# - git init: inicjalizuje repozytorium git w projekcie
# - git add <plik>: dodaje plik do śledzenia
# - git commit -m "komentarz": zapisuje zmiany z komentarzem
# - git push: wysyła zmiany na zdalne repozytorium
# - git pull: pobiera zmiany z repozytorium

# Jeśli masz już repozytorium na GitHubie, użyj:
# git remote add origin <url_repozytorium>
# git push -u origin master
