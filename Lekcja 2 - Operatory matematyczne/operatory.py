# W tej lekcji omówimy operatory matematyczne w Pythonie.

# Podstawowe operatory matematyczne:
a = 10
b = 5

# Dodawanie
dodawanie = a + b
print("Dodawanie:", dodawanie)

# Odejmowanie
odejmowanie = a - b
print("Odejmowanie:", odejmowanie)

# Mnożenie
mnożenie = a * b
print("Mnożenie:", mnożenie)

# Dzielenie
dzielenie = a / b
print("Dzielenie:", dzielenie)

# Dzielenie całkowite (wynik bez reszty)
dzielenie_całkowite = a // b
print("Dzielenie całkowite:", dzielenie_całkowite)

# Reszta z dzielenia (modulo)
modulo = a % b
print("Reszta z dzielenia:", modulo)

# Potęgowanie
potegowanie = a ** b
print("Potęgowanie:", potegowanie)

# Podsumowanie lekcji:
# - **Dodawanie (+)**, **Odejmowanie (-)**, **Mnożenie (*)**, **Dzielenie (/)**
# - **Dzielenie całkowite (//)** – wynik dzielenia bez reszty
# - **Modulo (%)** – reszta z dzielenia
# - **Potęgowanie (**) – potęga liczby
