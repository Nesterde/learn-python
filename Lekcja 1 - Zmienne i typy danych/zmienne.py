# W tej lekcji omówimy zmienne oraz typy danych w Pythonie.

# Zmienna to po prostu "pojemnik", w którym przechowujemy dane.
# W Pythonie nie musimy określać typu zmiennej z góry (jest dynamiczny).

# Zmienna 'wiek' przechowuje wartość liczbową, więc jest typu int (liczba całkowita).
wiek = 25

# Zmienna 'imie' przechowuje ciąg znaków, czyli tekst, co oznacza typ str.
imie = "Piotr"

# Zmienna 'wzrost' przechowuje wartość zmiennoprzecinkową, typ float.
wzrost = 1.75

# Zmienna 'czy_dorosly' to zmienna logiczna (bool), która może mieć dwie wartości: True lub False.
czy_dorosly = True

# Drukujemy wszystkie zmienne
print("Wiek:", wiek)
print("Imię:", imie)
print("Wzrost:", wzrost)
print("Czy dorosły:", czy_dorosly)

# Podsumowanie lekcji:
# - Zmienna to miejsce w pamięci, które przechowuje dane.
# - W Pythonie zmienne są dynamiczne, więc nie musimy określać ich typu przed użyciem.
# - W Pythonie mamy podstawowe typy danych:
#   - **int** – liczba całkowita,
#   - **float** – liczba zmiennoprzecinkowa,
#   - **str** – ciąg znaków (tekst),
#   - **bool** – wartość logiczna (True lub False).
